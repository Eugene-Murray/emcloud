    <h5 class="card-title">Considerations</h5>
    
    <ul class="list-group list-group-flush">
    <li class="list-group-item">Price: One of the first rules of trend following is that price is the main concern. Traders may use other indicators showing where price may go next or what it should be but as a general rule these should be disregarded. A trader need only be worried about what the market is doing, not what the market might do. The current price and only the price tells you what the market is doing.</li>
    <li class="list-group-item">Money management: Another decisive factor of trend following is not the timing of the trade or the indicator, but rather the decision of how much to trade over the course of the trend.</li>
    <li class="list-group-item">Risk control: Cut losses is the rule. This means that during periods of higher market volatility, the trading size is reduced. During losing periods, positions are reduced and trade size is cut back. The main objective is to preserve capital until more positive price trends reappear.</li>
    <li class="list-group-item">Rules: Trend following should be systematic. Price and time are pivotal at all times. This technique is not based on an analysis of fundamental supply and demand factors.</li>
    <li class="list-group-item">Diversification: Research published by hedge fund manager Andreas Clenow shows that cross asset diversification is an essential part of professional trend following.</li>
  </ul>
    
